function city_country(city: string, country: string) {
  return `"${city}, ${country}"`;
}

const pair1: string = city_country("Karachi", "Pakistan");
const pair2: string = city_country("Copenhagen", "Denmark");
const pair3: string = city_country("Istanbul", "Turkiye");

console.log(pair1);
console.log(pair2);
console.log(pair3);  
